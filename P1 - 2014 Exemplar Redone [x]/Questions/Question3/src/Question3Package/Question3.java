    private void btnQ3_1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQ3_1ActionPerformed
        String schoolCode = cbxSchool.getSelectedItem().toString();
        int counter = 0;
        for (int i = 0; i < arrSchoolAbrv.length; i++) {
            if (schoolCode.equals(arrSchoolAbrv[i])) {
                counter = i;
            }
        }
        String schoolName = arrSchoolNames[counter];
        String[] retrievedData = this.retrieveFileData();
        ArrayList<String> schoolData = new ArrayList<String>();
        for (String data : retrievedData) {
            String[] contents = data.split("#");
            String[] containedData = contents[0].split(";");
            String schoolAbbreviation = containedData[1];
            if (schoolAbbreviation.equals(schoolCode)) {
                schoolData.add(data);
            }
        }
        String[] schoolInfo = schoolData.toArray(new String[schoolData.size()]);
        int[][] schoolResults = new int[8][4];

        // Initializing Array to Zero
        for (int i = 0; i < schoolResults.length; i++) {
            for (int j = 0; j < 4; j++) {
                schoolResults[i][j] = 0;
            }
        }

        for (int i = 0; i < schoolResults.length; i++) {
            schoolResults[i][0] = i + 1;

            for (String data : schoolInfo) {
                String[] contents = data.split("#");
                String[] infoP1 = contents[0].split(";");
                String entryID = infoP1[0];
                String schoolAbv = infoP1[1];
                String[] infoP2 = contents[1].split(";");
                int place = Integer.parseInt(infoP2[1]);
                String[] participantData = infoP2[0].split("-");
                String event = participantData[0];
                String[] details = participantData[1].split(" ");
                String gender = details[0];
                String ageGroup = details[1];

                if (place == (i + 1)) {
                    switch (gender) {
                        case "Boys":
                            schoolResults[i][1]++;
                            break;
                        case "Girls":
                            schoolResults[i][2]++;
                    }
                    schoolResults[i][3]++;
                }
            }

        }

        String output = String.format("School: %s (%s)\n\n"
                + "%-15s%-15s%-15s%-15s\n", schoolName, schoolCode, "Place", "Boys", "Girls", "Total");
        for (int i = 0; i < schoolResults.length; i++) {
            int resultPlace = schoolResults[i][0];
            int boysResults = schoolResults[i][1];
            int girlsResults = schoolResults[i][2];
            int totalResults = schoolResults[i][3];
            output += String.format("%-15d%-15d%-15d%-15d\n", resultPlace, boysResults, girlsResults, totalResults);
        }
        txaOutput.setText(output);
    }//GEN-LAST:event_btnQ3_1ActionPerformed

    private void btnQ3_2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQ3_2ActionPerformed
        txaOutput.setText("Results of schools over the past three years : \n\n");
        String output = String.format("%-15s%-15s%-15s%-15s%-15s\n", "", "2012", "2013", "2014", "Average Points");

        // Done this calculation based method "on the fly" rather than creating a two-dimensional array and then reading from it
        /* It's normally much faster to do "on the fly" methods, it would be suitable to do in situations where 
         computing resources are minimal and where time is of the essence. */
        double highestAverage = 0.00;

        String[][] results = new String[arrSchoolAbrv.length][5];
        for (int i = 0; i < results.length; i++) {
            results[i][0] = arrSchoolAbrv[i];
            results[i][1] = arrSchoolResults[i][0] + "";
            results[i][2] = arrSchoolResults[i][1] + "";
            results[i][3] = arrSchoolResults[i][2] + "";
            double average = (arrSchoolResults[i][0] + arrSchoolResults[i][1] + arrSchoolResults[i][2]) / 3;
            results[i][4] = average + "";
            if (average > highestAverage) {
                highestAverage = average; // Updating Average
            }
        }

        // Determining schools that achieved the highest average points
        for (int i = 0; i < results.length; i++) {
            double schoolAverage = Double.parseDouble(results[i][4]);
            if (schoolAverage == highestAverage) {
                results[i][0] += "*";
            }
        }

        for (int i = 0; i < results.length; i++) {
            output += String.format("%-15s%-15s%-15s%-15s%-15s\n", results[i][0], results[i][1],
                    results[i][2], results[i][3], results[i][4]);
        }
        txaOutput.append(output);


    }//GEN-LAST:event_btnQ3_2ActionPerformed

