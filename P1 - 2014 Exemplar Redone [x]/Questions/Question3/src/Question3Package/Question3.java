    public String[] retrieveFileData() {
        String content = "";
        String[] textData = {};
        try {
            Scanner textFileScanner = new Scanner(new FileReader("Results.txt")).useDelimiter(",\\s*");
            while (textFileScanner.hasNext()) {
                content = textFileScanner.next();
            }
            textData = content.split("\r\n");
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        return textData;
    }

