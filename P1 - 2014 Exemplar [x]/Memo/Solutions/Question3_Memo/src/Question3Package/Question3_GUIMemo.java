    private void btnQ3_1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQ3_1ActionPerformed
        int posnSchool = cbxSchool.getSelectedIndex();
        String schoolAbr = arrSchoolAbrv[posnSchool];
        
        try {
            BufferedReader bf = new BufferedReader(new FileReader("Results.txt"));  //1

            for (int tel = 0; tel < 8; tel++) {
                arrBoys[tel] = 0;
                arrGirls[tel] = 0;
            }                                           //2
            String line = bf.readLine();
            while (line != null) {  //1

                line = line.replace(";", "#");
                String[] temp = line.split("#");       //2

                if (temp[1].equals(schoolAbr)) {  //1
                    if (temp[2].indexOf("Boys") >= 0) {  //1  boys or girls
                        int position = Integer.parseInt(temp[3]);  //1
                        arrBoys[position - 1]++;
                    }
                    if (temp[2].indexOf("Girls") >= 0) {
                        int position = Integer.parseInt(temp[3]);
                        arrGirls[position - 1]++; //2
                    }
                }
                line = bf.readLine();
            }
            String output = String.format("%-12s%-12s%-12s%-12s%n", "Place", "Boys", "Girls", "Total");
            txaOutput.setText("School: " + arrSchoolNames[posnSchool] + " (" + schoolAbr + ")\n" + output);  //1
            for (int tel = 0; tel < 8; tel++) {  //1
                output = String.format("%-12d%-12d%-12d%-12d%n", (tel + 1), arrBoys[tel], arrGirls[tel], (arrBoys[tel] + arrGirls[tel]));
                txaOutput.append(output);  //2
            }
        } catch (FileNotFoundException e) {
            System.out.println(e);
        } catch (Exception f) {
            System.out.println(f);
        }
    }//GEN-LAST:event_btnQ3_1ActionPerformed
    
    private void btnQ3_2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQ3_2ActionPerformed
        
        txaOutput.setText("");
        for (int i = 0; i < 8; i++) {
            arrSchoolResults[i][3] = 0;
        }
        
        int scoreHigh = 0;
        String output = String.format("%-20s%-12s%-12s%-12s%-12s%n", "", "2012", "2013", "2014", "Average points");
        txaOutput.append(output);                  //2
        
        for (int schCnt = 0; schCnt < 8; schCnt++) //1
        {
            for (int yrCnt = 0; yrCnt < 3; yrCnt++) //1
            {
                arrSchoolResults[schCnt][3] = arrSchoolResults[schCnt][3] + arrSchoolResults[schCnt][yrCnt];  //2
            }
            arrSchoolResults[schCnt][3] = arrSchoolResults[schCnt][3] / 3;//1
            if (scoreHigh < arrSchoolResults[schCnt][3]) {//1
                scoreHigh = arrSchoolResults[schCnt][3];//1
            }
        }

        //display

        for (int schCnt = 0; schCnt < 8; schCnt++) //1
        {
            String school = arrSchoolAbrv[schCnt];
            if (scoreHigh == arrSchoolResults[schCnt][3]) {//1
                school = arrSchoolAbrv[schCnt] + "*";//1
            }
            output = String.format("%-20s%-12d%-12d%-12d%-12d%n", school, arrSchoolResults[schCnt][0], arrSchoolResults[schCnt][1], arrSchoolResults[schCnt][2], arrSchoolResults[schCnt][3]);//2
            txaOutput.append(output);//1
        }
        
    }//GEN-LAST:event_btnQ3_2ActionPerformed
    
